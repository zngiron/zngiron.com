export const PORTFOLIO_DATA: WorkEntry[] = CLIENT_ORDER.map((clientName) => {
  const meta = CLIENT_META[clientName];
  const clientProjects = PROJECTS.filter((p) => p.client === clientName);
  const industry = meta.industry ?? clientProjects[0]?.industry ?? '—';

  return {
    id: meta.id,
    number: meta.number,
    client: clientName,
    industry,
    role: meta.role,
    year: meta.year,
    projects: clientProjects.map((p) => {
      const subBrandLabel = p.subBrand ? ` (${p.subBrand})` : '';
      return {
        name: `${p.brand}${subBrandLabel}`,
        details: p.project,
        tags: p.tags,
        link: p.url,
      };
    }),
  };
});
